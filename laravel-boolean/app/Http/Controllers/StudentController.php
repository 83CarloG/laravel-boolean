     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $newStudent = new Student;

        // inserisco valore per valore

        $newStudent->name = $data['name'];
        $newStudent->gender = $data['gender'];
        $newStudent->date_of_birth = $data['date_of_birth'];
        $newStudent->lastname = $data['lastname'];

        $newStudent->save();

        // aggiungi linea di codice per individuare l'ultimo inserimento

        return redirect()->route('students.show', $newStudent);

    }

